# 1. New Line (\n) - Moves text to the next line
print("Line 1\nLine 2")

# 2. Horizontal Tab (\t) - Adds a tab space (useful for columns)
print("Name:\tAhmad\nAge:\t25")

# 3. Backslash (\\) - Used to print a literal backslash
print("File Path: C:\\Users\\Ahmad\\Documents")

# 4. Single & Double Quotes (\' and \")
print("It\'s a sunny day.")
print("He said, \"Python is amazing!\"")

# 5. Backspace (\b) - Erases the character before it
print("Ahmad  \bnawaz") # Output: Ahmad nawaz (removes one space)

# 6. Carriage Return (\r) - Moves cursor to start and overwrites
print("Hello World\rPython") # Output: PythonWorld

# 7. Form Feed (\f) - Historically for printers (starts a new page)
print("Page 1\fPage 2")

# 8. Octal and Hexadecimal (\ooo and \xhh)
# Represents characters using their numeric codes
print("\x41\x42\x43") # Output: ABC (Hex for A, B, C)